export function getGiftRecommendations(interests: string[], count: number = 3): GiftIdea[] {
  if (interests.length === 0) {
    const shuffled = [...GIFT_DATABASE].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
  }
  
  const matchingGifts = GIFT_DATABASE.filter(gift =>
    gift.tags.some(tag => interests.includes(tag))
  );
  
  const shuffled = [...matchingGifts].sort(() => Math.random() - 0.5);
  
  if (shuffled.length >= count) {
    return shuffled.slice(0, count);
  }
  
  const remainingNeeded = count - shuffled.length;
  const nonMatching = GIFT_DATABASE.filter(
    gift => !shuffled.includes(gift)
  ).sort(() => Math.random() - 0.5);
  
  return [...shuffled, ...nonMatching.slice(0, remainingNeeded)];
}
