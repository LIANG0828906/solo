export function getWordsByCategory(category: CategoryKey): string[] {
  const found = wordCategories.find(c => c.key === category);
  return found ? found.words : [];
}

export function getRandomWord(category?: CategoryKey): string {
  let pool: string[] = [];
  if (category) {
    pool = getWordsByCategory(category);
  } else {
    pool = wordCategories.flatMap(c => c.words);
  }
  return pool[Math.floor(Math.random() * pool.length)];
}

export function validateCustomWord(word: string): boolean {
  if (!word) return false;
  const trimmed = word.trim();
  if (trimmed.length < 1 || trimmed.length > 10) return false;
  return /^[\u4e00-\u9fa5a-zA-Z]+$/.test(trimmed);
}

export function getCategoryOfWord(word: string): CategoryKey | null {
  for (const cat of wordCategories) {
    if (cat.words.includes(word)) return cat.key;
  }
  return null;
}

export function generateHints(word: string): string[] {
  const category = getCategoryOfWord(word);
  const hints: string[] = [];
  if (category) {
    const templates = hintTemplates[category];
    if (templates && templates.length > 0) {
      hints.push(templates[0]);
    }
  }
  const specific = specificHints[word];
  if (specific && specific.length > 0) {
    hints.push(...specific.slice(0, 3));
  }
  while (hints.length < 4) {
    hints.push(generateGenericHint(hints.length));
  }
  return hints.slice(0, 4);
}

function generateGenericHint(index: number): string {
  const genericHints = [
    '这是一个常见的名词',
    '大多数人都听说过它',
    '它在日常生活中可能会被提及',
    '它有自己独特的特征'
  ];
  return genericHints[index % genericHints.length];
}
