  async downloadHTML(params: LayoutParams, css: string): Promise<void> {
    const html = this.generateFullHTML(params, css);
    const blob = new Blob([html], { 
      type: 'text/html;charset=utf-8',
      endings: 'native'
    });
    const timestamp = this.generateTimestamp();
    const safeFontName = params.fontFamily.replace(/[^a-zA-Z0-9]/g, '_').toLowerCase();
    saveAs(blob, `typography_${safeFontName}_${timestamp}.html`);
  }

  async copyToClipboard(content: string): Promise<boolean> {
    try {
      if (navigator.clipboard && window.isSecureContext) {
        await navigator.clipboard.writeText(content);
        return true;
      }
      return this.fallbackCopy(content);
    } catch {
      return this.fallbackCopy(content);
    }
  }

  private fallbackCopy(content: string): boolean {
    try {
      const textArea = document.createElement('textarea');
      textArea.value = content;
      textArea.style.position = 'fixed';
      textArea.style.left = '-99999px';
      textArea.style.top = '-99999px';
      textArea.style.width = '1px';
      textArea.style.height = '1px';
      textArea.style.opacity = '0';
      document.body.appendChild(textArea);
      textArea.focus();
      textArea.select();
      textArea.setSelectionRange(0, 9999999);
      const successful = document.execCommand('copy');
      document.body.removeChild(textArea);
      return successful;
    } catch {
      return false;
    }
  }

  private generateTimestamp(): string {
    const now = new Date();
    const pad = (n: number) => String(n).padStart(2, '0');
    return (
      now.getFullYear().toString() +
      pad(now.getMonth() + 1) +
      pad(now.getDate()) +
      pad(now.getHours()) +
      pad(now.getMinutes()) +
      pad(now.getSeconds())
    );
  }

  private escapeHtml(text: string): string {
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#039;');
  }
