  private generateGoogleFontsLink(fontFamily: string): string {
    if (this.isSystemFont(fontFamily)) {
      return '';
    }
    const encodedFamily = encodeURIComponent(fontFamily).replace(/%20/g, '+');
    return `<link rel="preconnect" href="https://fonts.googleapis.com">
<link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
<link href="https://fonts.googleapis.com/css2?family=${encodedFamily}:ital,wght@0,400;0,700;1,400&display=swap" rel="stylesheet">`;
  }

  private generateFontFaceDeclaration(fontFamily: string): string {
    const safeFamily = fontFamily.includes(' ') ? `"${fontFamily}"` : fontFamily;
    if (this.isSystemFont(fontFamily)) {
      return `@font-face {
  font-family: ${safeFamily};
  src: local('${fontFamily}');
  font-display: swap;
}`;
    }
    return `@font-face {
  font-family: ${safeFamily};
  font-display: swap;
}`;
  }

