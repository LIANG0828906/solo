function buildSynonymMap(): Record<string, string[]> {
  const map: Record<string, string[]> = {};
  for (const group of SYNONYM_GROUPS) {
    for (let i = 0; i < group.length; i++) {
      const word = group[i].toLowerCase();
      const others = group.filter((_, j) => j !== i).map((w) => w.toLowerCase());
      if (!map[word]) {
        map[word] = [];
      }
      for (const o of others) {
        if (!map[word].includes(o)) {
          map[word].push(o);
        }
      }
    }
  }
  return map;
}

const SYNONYMS = buildSynonymMap();

export function getSynonyms(word: string): string[] {
  return SYNONYMS[word.toLowerCase()] || [];
}
