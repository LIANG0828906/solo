export function searchBuiltInBooks(query: string): BuiltInBook[] {
  if (!query.trim()) return [];
  const lowerQuery = query.toLowerCase();
  return builtInBooks.filter(
    (book) =>
      book.title.toLowerCase().includes(lowerQuery) ||
      book.author.toLowerCase().includes(lowerQuery) ||
      book.isbn.includes(query.trim())
  ).slice(0, 10);
}

export function getBookByIsbn(isbn: string): BuiltInBook | undefined {
  return builtInBooks.find((book) => book.isbn === isbn.trim());
}
