export const characters: Character[] = [];

Object.entries(radicalChars).forEach(([radical, chars]) => {
  chars.forEach(char => {
    characters.push({
      id: uuidv4(),
      char,
      radical: radical as Radical,
      strokeCount: strokeCounts[char] || Math.floor(Math.random() * 10) + 5
    });
  });
});

export const getCharactersByRadical = (radical: Radical): Character[] => {
  return characters.filter(c => c.radical === radical);
};
