const generateMoves = (pattern: number[][], count: number) => {
  const moves = [];
  for (let i = 0; i < Math.min(count, pattern.length); i++) {
    moves.push({ row: pattern[i][0], col: pattern[i][1] });
  }
  return moves;
};

