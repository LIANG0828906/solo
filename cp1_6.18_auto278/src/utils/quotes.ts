const usedIndices = new Set<number>();

export function getRandomQuote(): string {
  if (usedIndices.size >= quotes.length) {
    usedIndices.clear();
  }
  let index: number;
  do {
    index = Math.floor(Math.random() * quotes.length);
  } while (usedIndices.has(index));
  usedIndices.add(index);
  return quotes[index];
}
