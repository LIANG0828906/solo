import { InstrumentType, TrackState } from '../engine/audioEngine';

export interface Preset {
  name: string;
  bpm: number;
  masterVolume: number;
  tracks: Record<InstrumentType, TrackState>;
}

const createEmptyBeats = (): boolean[][] => {
  return Array(8).fill(null).map(() => Array(8).fill(false));
};

const createBeats = (pattern: number[][]): boolean[][] => {
  const beats = createEmptyBeats();
  pattern.forEach(([bar, beat]) => {
    if (bar < 8 && beat < 8) {
      beats[bar][beat] = true;
    }
  });
  return beats;
};

