export const getQuestionsByCountry = (country: string): QuizQuestion[] => {
  const questions = questionBank.filter(
    (q) => q.country === country
  );
  return questions.map((q) => ({ ...q, id: uuidv4() }));
};

export const getRandomQuestion = (country: string): QuizQuestion | null => {
  const questions = getQuestionsByCountry(country);
  if (questions.length === 0) return null;
  const randomIndex = Math.floor(Math.random() * questions.length);
  return questions[randomIndex];
};

export const getAvailableCountries = (): string[] => {
  const countries = new Set(questionBank.map((q) => q.country));
  return Array.from(countries);
};
