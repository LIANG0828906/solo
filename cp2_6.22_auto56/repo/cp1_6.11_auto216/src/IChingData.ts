const TRIGRAM_NAMES: Record<number, string> = {
  7: '乾', 0: '坤', 1: '震', 4: '艮', 2: '坎', 5: '离', 3: '兑', 6: '巽'
};

function trigramValue(lines: number[], start: number): number {
  return lines[start] + lines[start + 1] * 2 + lines[start + 2] * 4;
}

function upperLowerName(lines: number[]): string {
  const lower = TRIGRAM_NAMES[trigramValue(lines, 0)] || '';
  const upper = TRIGRAM_NAMES[trigramValue(lines, 3)] || '';
  return upper + lower;
}

