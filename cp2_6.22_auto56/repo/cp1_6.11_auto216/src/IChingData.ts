const HEXAGRAMS: HexagramInfo[] = HEXAGRAM_RAW.map(([number, name, lines, judgment, yaoTexts, changingTexts]) => ({
  number,
  name,
  symbol: String.fromCodePoint(0x4DC0 + number - 1),
  judgment,
  lines,
  yaoTexts,
  changingTexts
}));

export function getHexagramByPattern(lines: number[]): HexagramInfo | null {
  const value = lines[0] + lines[1] * 2 + lines[2] * 4 + lines[3] * 8 + lines[4] * 16 + lines[5] * 32;
  const index = PATTERN_TO_INDEX[value];
  if (index === undefined) return null;
  return HEXAGRAMS[index - 1] || null;
}

export function getHexagramByNumber(num: number): HexagramInfo | null {
  return HEXAGRAMS[num - 1] || null;
}

export function getChangedLines(lines: number[], changingIndices: number[]): number[] {
  const changed = [...lines];
  for (const idx of changingIndices) {
    changed[idx] = changed[idx] === 1 ? 0 : 1;
  }
  return changed;
}

export function generateFortune(yaoList: { type: string; isChanging: boolean }[], hexagramName: string): string {
  const yangCount = yaoList.filter(y => y.type === 'oldYang' || y.type === 'youngYang').length;
  const changingCount = yaoList.filter(y => y.isChanging).length;

  const fortunes: string[] = [];

  if (yangCount >= 4) {
    fortunes.push('阳刚之气充沛，行事当以刚健为先');
  } else if (yangCount <= 2) {
    fortunes.push('阴柔之质为主，宜以柔顺应事');
  } else {
    fortunes.push('阴阳交错，当审时度势而行');
  }

  if (changingCount === 0) {
    fortunes.push('无变爻之象，本卦定吉凶宜守成');
  } else if (changingCount === 1) {
    fortunes.push('一爻独变，事有转机当细察');
  } else if (changingCount <= 3) {
    fortunes.push('变爻较多，世事多变宜灵活应变');
  } else {
    fortunes.push('变爻甚多，大局将变当顺势而为');
  }

  fortunes.push(`${hexagramName}之象已成，静心体悟方得其真`);

  return fortunes.join('，');
}

