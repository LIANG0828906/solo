function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function pickRandomTags(): string[] {
  const shuffled = [...TAGS_POOL].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 3 + Math.floor(Math.random() * 3));
}

function generateReviews(): Review[] {
  const count = 3 + Math.floor(Math.random() * 3);
  const reviews: Review[] = [];
  const usedUsers = new Set<string>();

  for (let i = 0; i < count; i++) {
    let userName = pickRandom(REVIEW_USERS);
    while (usedUsers.has(userName)) {
      userName = pickRandom(REVIEW_USERS);
    }
    usedUsers.add(userName);

    reviews.push({
      id: uuidv4(),
      userName,
      rating: Math.round(3 + Math.random() * 2),
      content: pickRandom(REVIEW_CONTENTS)
    });
  }

  return reviews;
}

export function generateMockBooks(count: number = 36): Book[] {
  const books: Book[] = [];

  for (let i = 0; i < count; i++) {
    books.push({
      id: uuidv4(),
      title: TITLES[i % TITLES.length],
      author: AUTHORS[i % AUTHORS.length],
      category: CATEGORIES[i % CATEGORIES.length],
      tags: pickRandomTags(),
      description: DESCRIPTIONS[i % DESCRIPTIONS.length],
      publishYear: 1980 + Math.floor(Math.random() * 45),
      reviews: generateReviews()
    });
  }

  return books;
}
